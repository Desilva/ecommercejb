            	<h4>PUPUK ORGANIK:Jabar Masih Butuh Industri Pengolahan
            </div>
        </div>
        <div class="span11" style="text-align:justify">
        	<img src="images/asset/image2.png"s width="300" style="float:left">
            Bandung (bisnis-jabar.com)-Himpunan kerukunan Tari Indonesia (HKT)Jawa Barat mendesak pemerintah menggerjat pendirian industri pupuk organik
            Ketua HKTI Jabar Entang Satraatmadja mengemukakan saat ini kebutuhan pupuk organik yang dipasok dari industri di Javar masih di kisaran 80%, sisanya dari
            luar Jabar
            
            "Sekarang Industri pupuk tidak terpaku terhadap pupuk anorganik atau kimia akan tetapi pupuk organik pun menjadi salah satu pilihan utama petani,"katanya
            kepada Bisnis,Senin(29/4/2013)
            Peningkatan industri pupuk organnik, katanya dapat menutupi kebutuhan sektor usaha mukro untuk berkembang karena investasi tidak membutuhkan modal besar
            "Kalau pendirian industri pupuk anorganik memang membutuhkan biaya yang besar. Akan Tetapi, untuk organik bagi bisa diakses oleh sektor mikro," ujar Entang
            Oleh karena itu, HKTI Jabar meminta pemerintah  memberikan alternatif bagi pendirian industri pupuk organik bagi sekitar mikro agar produktivitas pertanian di
            di Jabar meningkat
            
            Selain itu, katanya, imabas dari industri pupuk organik dapat membantu menciptakan lahan terbuka hijau yang ramah lingkungan
            Pada perkembangan lain, penggunaan pupuk organik pada tanaman holtikulture di Kabutpaten Bandung mencapai 70% sehingga produk biasa
            maksimal (k32/k20/yri)
            
